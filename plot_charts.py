def _sum_float(values: list[float | None]) -> float:
    return round(sum(value or 0 for value in values), 2)


def aggregate_sessions(items: list[ReportSession]) -> dict[str, float | int]:
    est = _sum_float([s.est_hours for s in items])
    actual = _sum_float([s.actual_hours for s in items])
    saved = _sum_float([s.saved_hours for s in items])
    efficiency = round(saved / est * 100, 2) if est else 0
    return {
        "sessions": len(items),
        "est": est,
        "actual": actual,
        "saved": saved,
        "efficiency": efficiency,
    }


def group_aggregate(sessions: list[ReportSession], attr: str) -> list[tuple[str, dict[str, float | int]]]:
    groups: dict[str, list[ReportSession]] = defaultdict(list)
    for session in sessions:
        key = getattr(session, attr) or "(none)"
        groups[str(key)].append(session)
    rows = [(key, aggregate_sessions(items)) for key, items in groups.items()]
    rows.sort(key=lambda row: (-float(row[1]["saved"]), row[0]))
    return rows


def date_aggregate(sessions: list[ReportSession]) -> list[tuple[str, dict[str, float | int]]]:
    rows = group_aggregate(sessions, "date")

    def sort_key(row: tuple[str, dict[str, float | int]]) -> tuple[datetime, str]:
        value = row[0]
        for fmt in ("%d/%m/%Y", "%Y-%m-%d %H:%M:%S", "%Y-%m-%d"):
            try:
                return datetime.strptime(value, fmt), value
            except ValueError:
                pass
        return datetime.max, value

    return sorted(rows, key=sort_key)


def rating_counts_by_tool(sessions: list[ReportSession]) -> list[list[Any]]:
    tools = sorted({s.tool or "(không rõ)" for s in sessions})
    rows: list[list[Any]] = []
    for tool in tools:
        items = [s for s in sessions if (s.tool or "(không rõ)") == tool]
        counts = Counter()
        ratings: list[float] = []
        for session in items:
            if session.rating is None:
                continue
            rounded = int(round(session.rating))
            if 1 <= rounded <= 5:
                counts[rounded] += 1
                ratings.append(session.rating)
        total = sum(counts.values())
        avg = round(sum(ratings) / len(ratings), 2) if ratings else 0
        rows.append([tool, counts[1], counts[2], counts[3], counts[4], counts[5], total, avg])
    rows.sort(key=lambda row: (-row[6], row[0]))
    return rows


def collapsed_error_labels(sessions: list[ReportSession]) -> tuple[list[str], Counter[str]]:
    raw_counts: Counter[str] = Counter()
    for session in sessions:
        labels = session.error_labels or ["Unclassified"]
        for label in labels:
            raw_counts[label] += 1

    top = [label for label, _ in raw_counts.most_common(MAX_ERROR_LABELS_FOR_CHART)]
    collapsed: Counter[str] = Counter()
    for label, count in raw_counts.items():
        collapsed[label if label in top else "Other"] += count

    labels = [label for label in top if label in collapsed]
    if collapsed.get("Other"):
        labels.append("Other")
    return labels, collapsed


def error_staff_matrix(sessions: list[ReportSession], labels: list[str]) -> tuple[list[str], list[list[Any]]]:
    staff_list = sorted({s.staff for s in sessions})
    rows: list[list[Any]] = []
    top_set = set(labels) - {"Other"}
    for label in labels:
        row: list[Any] = [label]
        for staff in staff_list:
            count = 0
            for session in sessions:
                if session.staff != staff:
                    continue
                session_labels = session.error_labels or ["Unclassified"]
                if label == "Other":
                    count += sum(1 for item in session_labels if item not in top_set)
                elif label in session_labels:
                    count += 1
            row.append(count)
        rows.append(row)
    return staff_list, rows


def sdlc_staff_matrix(sessions: list[ReportSession]) -> tuple[list[str], list[list[Any]]]:
    staff_list = sorted({s.staff for s in sessions})
    rows: list[list[Any]] = []
    for category in SDLC_TAXONOMY:
        row: list[Any] = [category]
        for staff in staff_list:
            row.append(sum(1 for s in sessions if s.staff == staff and s.sdlc_category == category))
        rows.append(row)
    return staff_list, rows


def sdlc_task_chart_matrix(sessions: list[ReportSession]) -> tuple[list[str], list[list[Any]]]:
    task_counts_by_stage: dict[str, Counter[str]] = {category: Counter() for category in SDLC_TAXONOMY}
    for category in SDLC_TAXONOMY:
        stage_sessions = []
        for session in sessions:
            session_category = session.sdlc_category if session.sdlc_category in SDLC_TAXONOMY else "Other"
            if session_category == category:
                stage_sessions.append(session)
        stage_sessions.sort(key=lambda session: (session.title.casefold(), session.staff.casefold(), session.date))
        task_counts_by_stage[category].update(
            session.title or session.task_desc or "(unnamed task)"
            for session in stage_sessions
        )

    task_names: list[str] = []
    seen: set[str] = set()
    for category in SDLC_TAXONOMY:
        for task_name in task_counts_by_stage[category]:
            if task_name not in seen:
                seen.add(task_name)
                task_names.append(task_name)

    rows = [
        [category, *[task_counts_by_stage[category][task_name] for task_name in task_names]]
        for category in SDLC_TAXONOMY
    ]
    return task_names, rows


def sdlc_task_detail_rows(sessions: list[ReportSession]) -> list[list[Any]]:
    rows: list[list[Any]] = []
    for category in SDLC_TAXONOMY:
        stage_sessions = [
            session
            for session in sessions
            if (session.sdlc_category if session.sdlc_category in SDLC_TAXONOMY else "Other") == category
        ]
        stage_sessions.sort(key=lambda session: (session.title.casefold(), session.staff.casefold(), session.date))
        for session in stage_sessions:
            saved = session.saved_hours
            efficiency = round(saved / session.est_hours * 100, 2) if saved is not None and session.est_hours else 0
            rows.append([
                category,
                session.title or session.task_desc or "(unnamed task)",
                session.staff,
                session.date,
                session.est_hours or 0,
                session.actual_hours or 0,
                saved or 0,
                efficiency,
            ])
    return rows


