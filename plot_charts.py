def _call_openai(model: str, prompt: str, timeout: int = 300) -> str:
    base = (OPENAI_BASE_URL or "").rstrip("/")
    if not base:
        raise RuntimeError("OPENAI_BASE_URL is required unless --no-ai is used")

    is_azure = "cognitiveservices.azure.com" in base or "openai.azure.com" in base
    endpoints: list[tuple[str, dict[str, str], bool]] = []
    if is_azure:
        host = re.sub(r"/openai(/v1)?$", "", base).rstrip("/")
        # Keep report.py-compatible Azure deployment routing first.
        endpoints.append((
            f"{host}/openai/deployments/{model}/chat/completions?api-version=2024-12-01-preview",
            {"api-key": OPENAI_API_KEY or ""},
            False,
        ))
        # Some Azure/OpenAI-compatible gateways expose the v1 route directly
        # even when their hostname contains "cognitiveservices.azure.com".
        endpoints.append((
            f"{base}/chat/completions",
            {"api-key": OPENAI_API_KEY or ""},
            True,
        ))
    else:
        headers = {}
        if OPENAI_API_KEY:
            headers["Authorization"] = f"Bearer {OPENAI_API_KEY}"
        endpoints.append((f"{base}/chat/completions", headers, True))

    body_template: dict[str, Any] = {
        "messages": [{"role": "user", "content": prompt}],
        "temperature": 0.1,
        "max_completion_tokens": 4096,
        "response_format": {"type": "json_object"},
    }

    last_error: requests.RequestException | None = None
    for url, headers, include_model in endpoints:
        body = dict(body_template)
        if include_model:
            body["model"] = model

        variants: list[dict[str, Any]] = [body]

        no_response_format = dict(body)
        no_response_format.pop("response_format", None)
        variants.append(no_response_format)

        max_tokens_body = dict(no_response_format)
        max_tokens_body["max_tokens"] = max_tokens_body.pop("max_completion_tokens")
        variants.append(max_tokens_body)

        for candidate in variants:
            try:
                response = requests.post(url, headers=headers, json=candidate, timeout=timeout)
                response.raise_for_status()
                return response.json()["choices"][0]["message"]["content"]
            except requests.HTTPError as exc:
                last_error = exc
                status = exc.response.status_code if exc.response is not None else None
                # Retry alternate body shapes and, for Azure-like hosts, alternate
                # OpenAI-compatible routing on common schema/routing failures.
                if status not in {400, 404, 422}:
                    raise
            except requests.RequestException as exc:
                last_error = exc
                raise
    assert last_error is not None
    raise last_error


def _parse_json_object(raw: str) -> dict[str, Any]:
    text = re.sub(r"^```(?:json)?\s*|\s*```$", "", (raw or "").strip(), flags=re.MULTILINE)
    try:
        parsed = json.loads(text)
        if isinstance(parsed, dict):
            return parsed
    except json.JSONDecodeError:
        pass
    match = re.search(r"\{.*\}", text, flags=re.DOTALL)
    if not match:
        raise ValueError("Model response did not contain a JSON object")
    parsed = json.loads(match.group(0))
    if not isinstance(parsed, dict):
        raise ValueError("Model response JSON root was not an object")
    return parsed


def _coerce_labels(raw: Any, taxonomy: dict[str, str]) -> list[str]:
    if isinstance(raw, str):
        values = [part.strip() for part in re.split(r"[,;|]", raw)]
    elif isinstance(raw, list):
        values = [str(item).strip() for item in raw]
    else:
        values = []

    labels: list[str] = []
    known_casefold = {label.casefold(): label for label in taxonomy}
    for value in values:
        if not value:
            continue
        label = known_casefold.get(value.casefold(), value)
        if label not in labels:
            labels.append(label)
        if len(labels) >= MAX_LABELS_PER_SESSION:
            break
    return labels or ["Insufficient Lesson Data"]


def classify_sessions(
    sessions: list[ReportSession],
    model: str,
    cache_path: Path,
    batch_size: int,
    timeout: int,
) -> None:
    cache = _load_cache(cache_path)
    taxonomy = dict(ERROR_TAXONOMY)

    misses: list[ReportSession] = []
    hits = 0
    for session in sessions:
        key = f"{model}:{session.cache_hash}"
        cached = cache.get(key)
        if cached:
            session.error_labels = list(cached.get("error_labels") or ["Insufficient Lesson Data"])
            session.error_evidence = str(cached.get("error_evidence") or "")
            session.sdlc_category = str(cached.get("sdlc_category") or "Other")
            session.sdlc_confidence = _to_float(cached.get("sdlc_confidence"))
            session.sdlc_reason = str(cached.get("sdlc_reason") or "")
            hits += 1
            continue
        misses.append(session)

    print(f"🤖  Classifying errors + SDLC with {model}. Cache hits: {hits}/{len(sessions)}")

    for start in range(0, len(misses), batch_size):
        batch = misses[start:start + batch_size]
        print(f"  • Batch {start // batch_size + 1}: {len(batch)} session(s)")
        prompt = build_classifier_prompt(batch, taxonomy)
        raw = _call_openai(model=model, prompt=prompt, timeout=timeout)
        parsed = _parse_json_object(raw)

        for item in parsed.get("new_taxonomy", []) if isinstance(parsed.get("new_taxonomy"), list) else []:
            if not isinstance(item, dict):
                continue
            label = str(item.get("label") or "").strip()
            definition = str(item.get("definition") or "").strip()
            if label and label not in taxonomy:
                taxonomy[label] = definition or "Model-added prompt error label."

        by_id: dict[str, dict[str, Any]] = {}
        result_sessions = parsed.get("sessions")
        if isinstance(result_sessions, list):
            by_id = {
                str(item.get("id") or ""): item
                for item in result_sessions
                if isinstance(item, dict) and item.get("id")
            }

        for session in batch:
            item = by_id.get(session.row_id, {})
            for new_label in item.get("new_error_labels", []) if isinstance(item.get("new_error_labels"), list) else []:
                new_label_text = str(new_label).strip()
                if new_label_text and new_label_text not in taxonomy:
                    taxonomy[new_label_text] = "Model-added prompt error label."

            session.error_labels = _coerce_labels(item.get("error_labels"), taxonomy)
            session.error_evidence = str(item.get("error_evidence") or "").strip()
            sdlc_category = str(item.get("sdlc_category") or "Other").strip()
            if sdlc_category not in SDLC_TAXONOMY:
                sdlc_category = "Other"
            session.sdlc_category = sdlc_category
            session.sdlc_confidence = _to_float(item.get("sdlc_confidence"))
            session.sdlc_reason = str(item.get("sdlc_reason") or "").strip()

            cache[f"{model}:{session.cache_hash}"] = {
                "error_labels": session.error_labels,
                "error_evidence": session.error_evidence,
                "sdlc_category": session.sdlc_category,
                "sdlc_confidence": session.sdlc_confidence,
                "sdlc_reason": session.sdlc_reason,
            }
        _save_cache(cache_path, cache)

    print(f"✔  Classification complete. Cache: {cache_path}")


