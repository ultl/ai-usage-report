@dataclass
class ReportSession:
    row_id: str
    staff: str
    date: str
    title: str
    tool: str
    category: str
    task_desc: str
    rating: float | None
    est_hours: float | None
    actual_hours: float | None
    time_saved: float | None
    user_lesson: str
    tags: str
    source_file: str
    prompt: str = ""
    result: str = ""
    ai_lesson: str = ""
    error_labels: list[str] = field(default_factory=list)
    error_evidence: str = ""
    sdlc_category: str = "Other"
    sdlc_confidence: float | None = None
    sdlc_reason: str = ""

    @property
    def cache_hash(self) -> str:
        payload = {
            "version": PROMPT_VERSION,
            "staff": self.staff,
            "date": self.date,
            "title": self.title,
            "tool": self.tool,
            "category": self.category,
            "task_desc": self.task_desc,
            "prompt": self.prompt,
            "result": self.result,
            "user_lesson": self.user_lesson,
            "ai_lesson": self.ai_lesson,
        }
        raw = json.dumps(payload, ensure_ascii=False, sort_keys=True)
        return hashlib.sha1(raw.encode("utf-8")).hexdigest()

    @property
    def saved_hours(self) -> float | None:
        if self.time_saved is not None:
            return self.time_saved
        if self.est_hours is not None and self.actual_hours is not None:
            return self.est_hours - self.actual_hours
        return None


