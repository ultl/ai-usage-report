def _norm_header(value: Any) -> str:
    return re.sub(r"\s+", " ", str(value or "").strip())


def _find_header_row(ws, must_contain: str = "Staff", max_scan: int = 25) -> int:
    for row_idx in range(1, min(ws.max_row, max_scan) + 1):
        headers = {_norm_header(ws.cell(row=row_idx, column=col).value) for col in range(1, ws.max_column + 1)}
        if must_contain in headers:
            return row_idx
    raise ValueError(f"Could not find a header row containing {must_contain!r} in sheet {ws.title!r}")


def _header_map(ws, header_row: int) -> dict[str, int]:
    mapping: dict[str, int] = {}
    for col in range(1, ws.max_column + 1):
        header = _norm_header(ws.cell(row=header_row, column=col).value)
        if header:
            mapping[header] = col
    return mapping


def _value(ws, row: int, headers: dict[str, int], *names: str) -> Any:
    for name in names:
        col = headers.get(name)
        if col is not None:
            value = ws.cell(row=row, column=col).value
            if isinstance(value, str):
                value = value.strip()
            return value
    return None


def _to_float(value: Any) -> float | None:
    if value is None or value == "":
        return None
    if isinstance(value, (int, float)):
        return float(value)
    text = str(value).strip().replace(",", ".")
    match = re.search(r"-?\d+(?:\.\d+)?", text)
    if not match:
        return None
    try:
        return float(match.group(0))
    except ValueError:
        return None


def _fmt_date(value: Any) -> str:
    if value is None:
        return ""
    if isinstance(value, datetime):
        return value.strftime("%d/%m/%Y")
    return str(value).strip()


def _session_key(staff: str, date: str, title: str, tool: str) -> tuple[str, str, str, str]:
    return (
        staff.strip().casefold(),
        date.strip().casefold(),
        title.strip().casefold(),
        tool.strip().casefold(),
    )


def read_sessions(wb) -> list[ReportSession]:
    if RAW_LOG_SHEET not in wb.sheetnames:
        raise ValueError(f"Input workbook must contain {RAW_LOG_SHEET!r}; is this the output of report.py?")

    raw_ws = wb[RAW_LOG_SHEET]
    raw_header_row = _find_header_row(raw_ws)
    raw_headers = _header_map(raw_ws, raw_header_row)

    required = ["Staff", "Ngày", "Tên Phiên", "Công Cụ", "Mô Tả", "Rating", "EST (h)", "Actual (h)"]
    missing = [header for header in required if header not in raw_headers]
    if missing:
        raise ValueError(
            "The report workbook is missing current Raw Log columns required for charts: "
            + ", ".join(missing)
            + ". Regenerate it with the current report.py first."
        )

    sessions: list[ReportSession] = []
    for row in range(raw_header_row + 1, raw_ws.max_row + 1):
        staff = str(_value(raw_ws, row, raw_headers, "Staff") or "").strip()
        title = str(_value(raw_ws, row, raw_headers, "Tên Phiên") or "").strip()
        tool = str(_value(raw_ws, row, raw_headers, "Công Cụ") or "").strip()
        if not staff and not title and not tool:
            continue
        sessions.append(
            ReportSession(
                row_id=f"S{len(sessions) + 1}",
                staff=staff or "(không rõ)",
                date=_fmt_date(_value(raw_ws, row, raw_headers, "Ngày")),
                title=title,
                tool=tool or "(không rõ)",
                category=str(_value(raw_ws, row, raw_headers, "Danh Mục") or "").strip(),
                task_desc=str(_value(raw_ws, row, raw_headers, "Mô Tả") or "").strip(),
                rating=_to_float(_value(raw_ws, row, raw_headers, "Rating")),
                est_hours=_to_float(_value(raw_ws, row, raw_headers, "EST (h)")),
                actual_hours=_to_float(_value(raw_ws, row, raw_headers, "Actual (h)")),
                time_saved=_to_float(_value(raw_ws, row, raw_headers, "Tiết Kiệm (h)", "Giờ Tiết Kiệm")),
                user_lesson=str(_value(raw_ws, row, raw_headers, "Bài Học Người Dùng") or "").strip(),
                tags=str(_value(raw_ws, row, raw_headers, "Tags") or "").strip(),
                source_file=str(_value(raw_ws, row, raw_headers, "Source File") or "").strip(),
            )
        )

    if not sessions:
        raise ValueError(f"No sessions found in {RAW_LOG_SHEET!r}")

    enrich_sessions_from_ai_sheet(wb, sessions)
    return sessions


def enrich_sessions_from_ai_sheet(wb, sessions: list[ReportSession]) -> None:
    if AI_COMPARE_SHEET not in wb.sheetnames:
        return

    ai_ws = wb[AI_COMPARE_SHEET]
    header_row = _find_header_row(ai_ws)
    headers = _header_map(ai_ws, header_row)

    by_key: dict[tuple[str, str, str, str], dict[str, str]] = {}
    ordered: list[dict[str, str]] = []
    for row in range(header_row + 1, ai_ws.max_row + 1):
        staff = str(_value(ai_ws, row, headers, "Staff") or "").strip()
        title = str(_value(ai_ws, row, headers, "Tên Phiên") or "").strip()
        tool = str(_value(ai_ws, row, headers, "Công Cụ") or "").strip()
        if not staff and not title and not tool:
            continue

        data = {
            "staff": staff,
            "date": _fmt_date(_value(ai_ws, row, headers, "Ngày")),
            "title": title,
            "tool": tool,
            "task_desc": str(_value(ai_ws, row, headers, "Mô Tả Nhiệm Vụ") or "").strip(),
            "prompt": str(_value(ai_ws, row, headers, "Prompt Chính") or "").strip(),
            "result": str(_value(ai_ws, row, headers, "Kết Quả") or "").strip(),
            "user_lesson": str(_value(ai_ws, row, headers, "Bài Học Người Dùng") or "").strip(),
            "ai_lesson": str(_value(ai_ws, row, headers, "Bài Học AI Suy Luận") or "").strip(),
        }
        ordered.append(data)
        by_key[_session_key(data["staff"], data["date"], data["title"], data["tool"])] = data

    for idx, session in enumerate(sessions):
        data = by_key.get(_session_key(session.staff, session.date, session.title, session.tool))
        if data is None and idx < len(ordered):
            data = ordered[idx]
        if not data:
            continue
        session.task_desc = session.task_desc or data.get("task_desc", "")
        session.prompt = data.get("prompt", "")
        session.result = data.get("result", "")
        session.user_lesson = session.user_lesson or data.get("user_lesson", "")
        session.ai_lesson = data.get("ai_lesson", "")


