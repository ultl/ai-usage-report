def default_output_path(input_path: Path) -> Path:
    return input_path.with_name(f"{input_path.stem}_with_charts{input_path.suffix}")


def parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Create an enriched Excel workbook with charts from report.py output.",
    )
    parser.add_argument("report", type=Path, help="Input .xlsx workbook generated by report.py")
    parser.add_argument("-o", "--output", type=Path, help="Output enriched .xlsx workbook")
    parser.add_argument(
        "--model",
        default="qwen2.5:7b",
        help="OpenAI-compatible model for error + SDLC classification (default: qwen2.5:7b)",
    )
    parser.add_argument("--cache", type=Path, default=CACHE_PATH, help=f"AI classification cache (default: {CACHE_PATH})")
    parser.add_argument("--batch-size", type=int, default=20, help="Sessions per AI classification batch (default: 20)")
    parser.add_argument("--timeout", type=int, default=300, help="AI request timeout in seconds (default: 300)")
    parser.add_argument("--no-ai", action="store_true", help="Skip AI classification and mark rows as unclassified")
    return parser.parse_args(argv)


def main(argv: list[str] | None = None) -> int:
    args = parse_args(argv)
    input_path = args.report
    output_path = args.output or default_output_path(input_path)

    if not input_path.exists():
        print(f"Input workbook not found: {input_path}", file=sys.stderr)
        return 1
    if input_path.resolve() == output_path.resolve():
        print("Output path must be different from the input path so the original report remains unchanged.", file=sys.stderr)
        return 1
    if args.batch_size < 1:
        print("--batch-size must be >= 1", file=sys.stderr)
        return 1

    print(f"📂  Loading report workbook: {input_path}")
    wb = load_workbook(input_path)
    sessions = read_sessions(wb)
    print(f"✔  Parsed {len(sessions)} session(s) from {RAW_LOG_SHEET!r}")

    if args.no_ai:
        mark_unclassified(sessions)
        print("⏭  AI classification skipped (--no-ai)")
    else:
        try:
            classify_sessions(
                sessions=sessions,
                model=args.model,
                cache_path=args.cache,
                batch_size=args.batch_size,
                timeout=args.timeout,
            )
        except requests.HTTPError as exc:
            response = exc.response
            detail = ""
            if response is not None:
                detail = f" ({response.status_code} {response.reason})"
                body = response.text.strip()
                if body:
                    detail += f": {body[:500]}"
            print(f"AI classification failed{detail}", file=sys.stderr)
            print(
                "Check OPENAI_BASE_URL/OPENAI_API_KEY and --model, or run with --no-ai for chart-only output.",
                file=sys.stderr,
            )
            return 1
        except Exception as exc:
            print(f"AI classification failed: {exc}", file=sys.stderr)
            print(
                "Check OPENAI_BASE_URL/OPENAI_API_KEY and --model, or run with --no-ai for chart-only output.",
                file=sys.stderr,
            )
            return 1

    print("📊  Building chart sheets...")
    build_chart_workbook(wb, sessions)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    wb.save(output_path)
    print(f"✔  Enriched workbook saved to: {output_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
